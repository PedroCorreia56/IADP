#%% determine if a letter is a vowel
vowels = ('a', 'e', 'i', 'o', 'u')

for vowel in vowels:
    print(vowel)

letter = input("Letter: ")

if letter in vowels:
    print(f'The letter {letter} is a vowel')
else:
    print(f'The letter {letter} is not a vowel')
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    